package ss1_introduction_to_java.thuc_hanh;

public class Varible_Data_type {
    public static void main(String[] args) {
        int a = 10;
        float b = 1.0f;
        double c = 1.0;
        char d = 'a';
        boolean e = true;
        String f = "Hello";
        System.out.println("a la: " + a);
        System.out.println("b la: " + b);
        System.out.println("c la: " + c);
        System.out.println("d la: " + d);
        System.out.println("e la: " + e);
        System.out.println("f la: " + f);
    }
}
